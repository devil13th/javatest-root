package com.thd.spring.aop.aspectexpression;

public class User {
	private String user;
	private String pwd;
	
	public User(){
		
	}
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
}
